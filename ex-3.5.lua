-- Exercise 3.5: The number 12.7 is equal to the fraction 127/10, where
-- the denominator is a power of ten. Can you express it as a common
-- fraction where the denominator is a power of two?

-- No, because 127 is prime and 10 is 2 * 5, therefore the fraction is
-- already in its irreducible form.

-- What about the number 5.5?

-- Yes, because 5.5 is 55/10, that is, (5 * 11) / (2 * 5), and
-- therefore its irreducible form is 11/2.
